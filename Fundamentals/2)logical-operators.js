//      JavaScript Logical Operators

//        Operator	Description

//            &&	 and
//            ||	  or
//             !      not

// && and

if (1 === 1 && 2 === 2) {
  console.log("these both are true");
}

if (true && true) {
  console.log("equal and same values");
}

// || or

if (2 === 2 || 3 === 2) {
  console.log("One of them is true");
}

